"""Scaffold a paper-library-backed paper navigation site.

This module deliberately generates a small Next.js app skeleton rather than a
one-off pile of components. PaperScope owns the scholarly paper-reader surface;
LocalEvidence can call the same generator in medical mode and layer clinical
navigation around it.
"""

from __future__ import annotations

from dataclasses import asdict, dataclass
from pathlib import Path
import json
import re
from typing import Literal


Mode = Literal["academic", "medical"]


@dataclass(frozen=True)
class PaperSiteConfig:
    """Configuration written into a generated paper-site scaffold."""

    project_name: str
    title: str
    mode: Mode = "academic"
    paperlibrary_root: str = "../paper-library"
    manuscript_path: str = "src/content/manuscript.md"
    references_path: str = "src/content/references.json"
    require_auth: bool = True


def scaffold_paper_site(config: PaperSiteConfig, out_dir: Path, *, force: bool = False) -> list[Path]:
    """Create a Next.js paper-reader scaffold and return written files.

    The scaffold is intentionally data-first. Generated callers replace
    ``src/content/manuscript.md`` and ``src/content/references.json`` with their
    project export, while the reader UI and CSS stay consistent across projects.
    """

    out_dir = out_dir.resolve()
    if out_dir.exists() and any(out_dir.iterdir()) and not force:
        raise FileExistsError(f"{out_dir} is not empty; pass force=True to overwrite scaffold files")

    files = {
        "README.md": _readme(config),
        "package.json": _package_json(config),
        "next.config.ts": "const nextConfig = {};\n\nexport default nextConfig;\n",
        "next-env.d.ts": '/// <reference types="next" />\n/// <reference types="next/image-types/global" />\n\n// This file is generated by PaperScope and maintained by Next.js.\n',
        "tsconfig.json": _tsconfig_json(),
        "src/app/layout.tsx": _layout_tsx(config),
        "src/app/page.tsx": _page_tsx(),
        "src/app/PaperReader.tsx": _paper_reader_tsx(),
        "src/app/globals.css": SHARED_CSS,
        "src/content/manuscript.md": _sample_manuscript(config),
        "src/content/references.json": _sample_references(),
        "src/content/details.json": _sample_details(),
        "paper-site.config.json": json.dumps(asdict(config), indent=2) + "\n",
    }

    written: list[Path] = []
    for relative, content in files.items():
        target = out_dir / relative
        target.parent.mkdir(parents=True, exist_ok=True)
        if target.exists() and not force:
            raise FileExistsError(f"{target} exists; pass force=True to overwrite")
        target.write_text(content, encoding="utf-8")
        written.append(target)
    return written


def slugify(value: str) -> str:
    slug = re.sub(r"[^a-z0-9]+", "-", value.lower()).strip("-")
    return slug or "paper-site"


def _readme(config: PaperSiteConfig) -> str:
    label = "PaperScope" if config.mode == "academic" else "LocalEvidence"
    return f"""# {config.title}

Generated {label} paper-navigation site scaffold.

Shared contract:
- MD3/light-dark token palette: the shared MD3 visual contract used across the author's paper sites.
- Native web manuscript as the reading surface; PDFs are downloads only.
- Inline citation controls open the side panel first, with explicit external links inside the panel.
- More-detail markers are inline annotations, not callout blocks.
- Reference records carry paper-library status from `{config.paperlibrary_root}`
  (a permanent library at ~/paper-library by convention).
- Reference records should include reader-facing use notes when available:
  `role`, `what`, `why`, `caution`, and deduped `contexts`.
- Reference records can include `native_href` and `source_href`; citation clicks
  still open the sidebar first, and those external/native links live inside it.

Replace:
- `{config.manuscript_path}` with the rendered manuscript markdown.
- `{config.references_path}` with paper-library-backed citation records.

Optional:
- Add `src/content/sections.json` when a project needs subtabs. Each record
  can point to `manuscriptPath`/`manuscript_path` or carry inline `manuscript`.
  First visits open at the top; returning to a subtab restores its scroll.

Run:

```bash
npm install
npm run dev
```
"""


