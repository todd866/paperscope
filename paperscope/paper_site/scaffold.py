def _page_tsx() -> str:
    return '''import fs from "node:fs";
import path from "node:path";
import PaperReader, { type DetailHighlight, type PaperSection, type Reference } from "./PaperReader";

type SectionRecord = {
  id: string;
  label: string;
  eyebrow?: string;
  manuscript?: string;
  manuscriptPath?: string;
  manuscript_path?: string;
};

function readSections(): PaperSection[] | undefined {
  const sectionsPath = path.join(process.cwd(), "src/content/sections.json");
  if (!fs.existsSync(sectionsPath)) return undefined;

  const records = JSON.parse(fs.readFileSync(sectionsPath, "utf8")) as SectionRecord[];
  return records.map((record) => {
    const manuscript =
      record.manuscript ??
      fs.readFileSync(
        path.join(process.cwd(), record.manuscriptPath ?? record.manuscript_path ?? `src/content/${record.id}.md`),
        "utf8"
      );
    return { id: record.id, label: record.label, eyebrow: record.eyebrow, manuscript };
  });
}

function readContent() {
  const manuscript = fs.readFileSync(path.join(process.cwd(), "src/content/manuscript.md"), "utf8");
  const references = JSON.parse(fs.readFileSync(path.join(process.cwd(), "src/content/references.json"), "utf8")) as Reference[];
  const details = JSON.parse(fs.readFileSync(path.join(process.cwd(), "src/content/details.json"), "utf8")) as DetailHighlight[];
  const sections = readSections();
  return { manuscript, references, details, sections };
}

export default function Page() {
  const { manuscript, references, details, sections } = readContent();
  return <PaperReader manuscript={manuscript} references={references} details={details} sections={sections} />;
}
'''


